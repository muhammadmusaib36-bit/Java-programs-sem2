class exampleofdatatypes {
    public static void main(String[] args) {

        System.out.println("Example of Primitive Data type");

        int number = 36;
        double marks = 99.5;
        char grade = 'A';
        boolean isPass = true;

        System.out.println(number);
        System.out.println(marks);
        System.out.println(grade);
        System.out.println(isPass);

        System.out.println("<=============================================>");

        System.out.println("Example of Non-Primitive Data type");

        String name = "Moiz";
        int[] obt_marks = {85, 90, 78};

        System.out.println(name);
        System.out.println(obt_marks[0]);
    }
}